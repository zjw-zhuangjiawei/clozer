#[cfg(test)]
mod tests {
    use super::*;
    use std::collections::HashSet;

    #[test]
    fn all_locales_have_same_keys() {
        let en_keys: HashSet<&str> = TRANSLATIONS[0].1.iter().map(|(k, _)| *k).collect();

        for (locale, entries) in TRANSLATIONS.iter().skip(1) {
            let keys: HashSet<&str> = entries.iter().map(|(k, _)| *k).collect();
            let missing: Vec<_> = en_keys.difference(&keys).collect();
            let extra: Vec<_> = keys.difference(&en_keys).collect();
            assert!(
                missing.is_empty() && extra.is_empty(),
                "Locale {} has mismatched keys.\nMissing: {:?}\nExtra: {:?}",
                locale,
                missing,
                extra
            );
        }
    }

    #[test]
    fn no_empty_values() {
        for (locale, entries) in TRANSLATIONS {
            for (key, value) in *entries {
                assert!(
                    !value.is_empty(),
                    "Locale {} key '{}' has empty value",
                    locale,
                    key
                );
            }
        }
    }
}
